def Color(r=0, g=0, b=0):
    """ The main color generating function."""
    # Check if arguments aren't greater then 255
    if r > 255 or g > 255 or b > 255:
        raise ValueError("PGX.color.Color() must have all arguments less than 256.")
    else:
        return (r, g, b)


